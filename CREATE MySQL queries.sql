CREATE TABLE LGroundLayer(
	id INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    XPos INT NOT NULL UNIQUE,
    YPos INT NOT NULL UNIQUE,
    stock_id VARCHAR(45)
);

-- Underground SuperLayer tables
CREATE TABLE UMobLayer(
	id INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    XPos INT NOT NULL UNIQUE,
    YPos INT NOT NULL UNIQUE,
    stock_id VARCHAR(45)
);

CREATE TABLE UStructureLayer(
	id INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
